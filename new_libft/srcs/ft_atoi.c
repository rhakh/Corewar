#include "libft.h"

int		ft_atoi(const char *str)
{
	int	res;
	int	sign;

	res = 0;
	sign = 1;
	while ((9 <= *str && *str <= 13) || *str == 32)
		str++;
	if (*str == '-' || *str == '+')
		if (*(str + 1) >= '0' && *(str + 1) <= '9' && *(str + 1) != '\0')
		{
			sign = 44 - *str;
			str++;
		}
	while (*str >= '0' && *str <= '9' && *str != '\0')
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	return (res * sign);
}
